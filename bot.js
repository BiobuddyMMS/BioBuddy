let secretAnimal = pickRandomAnimal();

function pickRandomAnimal() {
  const keys = Object.keys(animalData);
  return keys[Math.floor(Math.random() * keys.length)];
}

function getAnimalInfo(animalName) {
  const info = animalData[animalName];
  if (!info) return "😥 ไม่พบข้อมูลของสัตว์นี้ในระบบ BioBuddy";
  let reply = `📚 ข้อมูลของ ${animalName}\n`;
  for (let key in info) {
    reply += `• ${key}: ${info[key]}\n`;
  }
  return reply;
}

function askYesNoQuestion(question) {
  const info = animalData[secretAnimal];
  const matchedKey = Object.keys(info).find(key => question.includes(key));
  if (!matchedKey) return "🤔 คำถามไม่ตรงกับลักษณะในระบบ ลองใหม่อีกครั้งน้า~";
  return info[matchedKey] === "ใช่" ? "✅ ใช่" : "❌ ไม่ใช่";
}

function guessAnimal(name) {
  if (name === secretAnimal) {
    const result = `🎉 ถูกต้อง! สัตว์ลับคือ ${secretAnimal}`;
    secretAnimal = pickRandomAnimal(); // รีเซ็ตสัตว์ลับใหม่
    return result;
  } else {
    return `❌ ยังไม่ใช่ ${name} นะ ลองใหม่อีกที!`;
  }
}

function getRules() {
  return `🎲 กติกาเกม Who is it? (เวอร์ชัน BioBuddy):
1. ระบบจะสุ่มสัตว์ลับไว้ 1 ตัว
2. คุณสามารถถามคำถามแบบ 'ใช่/ไม่ใช่' ได้ เช่น 'มีขา 6 ขาไหม?'
3. ทายชื่อสัตว์ได้ตลอด ถ้าทายถูก เกมจะจบ และเริ่มใหม่ทันที!
4. พิมพ์ 'กติกา' เพื่อดูคำอธิบายนี้อีกครั้ง 😉`;
}

function handleMessage(input) {
  input = input.trim();
  if (input.startsWith("ข้อมูล ")) {
    return getAnimalInfo(input.replace("ข้อมูล ", ""));
  } else if (input.startsWith("ทายว่าเป็น ")) {
    return guessAnimal(input.replace("ทายว่าเป็น ", ""));
  } else if (input.includes("ไหม") || input.includes("หรือไม่")) {
    return askYesNoQuestion(input);
  } else if (input === "กติกา") {
    return getRules();
  } else if (input === "ทอยลูกเต๋า") {
    return `🎲 คุณได้เลข ${Math.floor(Math.random() * 6) + 1}`;
  } else {
    return "🤖 พิมพ์ 'ข้อมูล <ชื่อสัตว์>' หรือ 'ทายว่าเป็น <ชื่อ>' หรือถามลักษณะได้น้า~";
  }
}
